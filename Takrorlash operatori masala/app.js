function Car(make, speed) {
  this.make = make;
  this.speed = speed;
  this.accelerate = function () {
    console.log(`${this.make} tezligi ${(this.speed += 10)}km/h ga oshdi.`);
  };
  this.brake = function () {
    console.log(`${this.make} tezligi ${(this.speed -= 5)}km / h ga kamaydi.`);
  };
}

